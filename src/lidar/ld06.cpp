LD06::LD06(HardwareSerial &serial, uint8_t rxPin, uint8_t pwmPin)
    : _serial(serial), _rxPin(rxPin), _pwmPin(pwmPin),
      _idx(0), _synced(false), _pendingCnt(0), _lastAngle(-1.0f),
      _scanCnt(0), _rpm(0.0f)
{
    _mux = portMUX_INITIALIZER_UNLOCKED;
}

void LD06::begin() {
    _serial.begin(LIDAR_BAUD, SERIAL_8N1, _rxPin, -1);  // TX non câblé, GPIO libéré

    // PWM moteur LIDAR : 10 kHz, duty ~50% (canal LEDC 0, résolution 8 bits)
    ledcSetup(0, LIDAR_PWM_FREQ_HZ, 8);
    ledcAttachPin(_pwmPin, 0);
    ledcWrite(0, LIDAR_PWM_DUTY);
}

void LD06::update() {
    while (_serial.available()) {
        processByte((uint8_t)_serial.read());
    }
}

void LD06::processByte(uint8_t b) {
    if (!_synced) {
        if (b == LD06_HEADER) {
            _raw[0] = b;
            _idx    = 1;
            _synced = true;
        }
        return;
    }

    _raw[_idx++] = b;

    if (_idx == 2 && b != LD06_VER_LEN) {
        // Faux départ : recommencer la recherche du header
        _idx    = 0;
        _synced = false;
        return;
    }

    if (_idx == LD06_PACKET_LEN) {
        parsePacket();
        _idx    = 0;
        _synced = false;
    }
}

bool LD06::parsePacket() {
    // Vérification CRC (porte sur les 46 premiers octets)
    if (crc8(_raw, LD06_PACKET_LEN - 1) != _raw[LD06_PACKET_LEN - 1]) {
        return false;
    }

    uint16_t speed_dps  = (uint16_t)(_raw[3] << 8 | _raw[2]);
    uint16_t startAngle = (uint16_t)(_raw[5] << 8 | _raw[4]);
    uint16_t endAngle   = (uint16_t)(_raw[43] << 8 | _raw[42]);

    _rpm = speed_dps / 6.0f;   // degrés/s ÷ 6 = tours/min

    // Interpolation angulaire linéaire sur les 12 points du paquet
    float startDeg = startAngle * 0.01f;
    float endDeg   = endAngle   * 0.01f;
    if (endDeg < startDeg) endDeg += 360.0f;   // wrap autour de 0°

    float step = (endDeg - startDeg) / (LD06_PTS_PER_PKT - 1);

    for (int i = 0; i < LD06_PTS_PER_PKT; i++) {
        uint8_t *p        = &_raw[6 + i * 3];
        uint16_t dist_mm  = (uint16_t)(p[1] << 8 | p[0]);
        uint8_t  conf     = p[2];

        float angle = startDeg + i * step;
        if (angle >= 360.0f) angle -= 360.0f;

        if (_pendingCnt < LD06_SCAN_BUF_SIZE) {
            _pending[_pendingCnt++] = { angle, dist_mm, conf };
        }
    }

    // Détection fin de révolution : on détecte un retour à 0° (passage endAngle → prochain startAngle < endAngle)
    float endDegWrapped = endAngle * 0.01f;
    if (_lastAngle > 350.0f && endDegWrapped < 10.0f) {
        // Publication du scan complet (thread-safe)
        portENTER_CRITICAL(&_mux);
        memcpy(_scan, _pending, _pendingCnt * sizeof(LidarPoint));
        _scanCnt = _pendingCnt;
        portEXIT_CRITICAL(&_mux);

        _pendingCnt = 0;
    }
    _lastAngle = endDegWrapped;

    return true;
}

uint16_t LD06::getScan(LidarPoint *dst, uint16_t maxPts) {
    portENTER_CRITICAL(&_mux);
    uint16_t n = (_scanCnt < maxPts) ? _scanCnt : maxPts;
    memcpy(dst, _scan, n * sizeof(LidarPoint));
    portEXIT_CRITICAL(&_mux);
    return n;
}

uint8_t LD06::crc8(const uint8_t *data, uint8_t len) {
    uint8_t crc = 0;
    for (uint8_t i = 0; i < len; i++) {
        crc = kCrcTable[crc ^ data[i]];
    }
    return crc;
}
